# This is is sorting  in python


colors = ['blue', 'white', 'yellow', 'green']
print(colors)
print("\n----------------------------------------------------------------")

#sorts  the colors in the alphabetical order
colors.sort()
print("\nHere is a sorted list of colors")
print(colors)
print("\n----------------------------------------------------------------")

#reverses the color's ordering
colors.sort(reverse=True)
print("\nHere is a reversed sorted list of colors")
print(colors)
print("\n----------------------------------------------------------------")